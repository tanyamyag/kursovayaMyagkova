// Импортируем функцию createApp из библиотеки Vue
// Она используется для создания экземпляра (объекта) приложения Vue
import { createApp } from 'vue'

// Импортируем функцию createHead из пакета @vueuse/head
// Этот плагин позволяет динамически изменять содержимое <head> страницы
// (например, <title>, <meta>, <link> и т.д.)
import { createHead } from '@vueuse/head'

// Импортируем корневой компонент приложения (основной компонент)
import App from './App.vue'

// Импортируем конфигурацию маршрутизатора (Vue Router)
// Она отвечает за навигацию между страницами приложения без перезагрузки
import router from './router'

// Импортируем глобальные стили приложения
// Этот CSS-файл применяется ко всем компонентам проекта
import './assets/main.css'

// Создаём экземпляр приложения Vue, используя главный компонент App
const app = createApp(App)

// Создаём экземпляр плагина для управления содержимым <head> документа
const head = createHead()

// Подключаем маршрутизатор к приложению
// Это позволяет использовать навигацию между страницами (router-link, router-view)
app.use(router)

// Подключаем плагин для управления <head> (заголовки, метатеги и т.д.)
app.use(head)

// Монтируем (встраиваем) приложение в HTML-документ
// Приложение будет отображено внутри элемента <div id="app"></div> в index.html
app.mount('#app')
